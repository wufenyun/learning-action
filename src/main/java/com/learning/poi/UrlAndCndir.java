package com.learning.poi;

public class UrlAndCndir {

    private String url;
    private String cndir;
    private int isValidCndir;

    public String getUrl() {
        return url;
    }

    public void setUrl(String url) {
        this.url = url;
    }

    public String getCndir() {
        return cndir;
    }

    public void setCndir(String cndir) {
        this.cndir = cndir;
    }

    public int getIsValidCndir() {
        return isValidCndir;
    }

    public void setIsValidCndir(int isValidCndir) {
        this.isValidCndir = isValidCndir;
    }
}
